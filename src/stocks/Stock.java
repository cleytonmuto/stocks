package stocks;

public class Stock {
	
	private String ticket;
	private double value;
	
	public Stock( ) {
		
	}

	public String getTicket() {
		return ticket;
	}

	public void setTicket(String ticket) {
		this.ticket = ticket;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
	
}

